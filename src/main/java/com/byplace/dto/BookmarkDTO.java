package com.byplace.dto;

public class BookmarkDTO {
	long bookmark_no, restaurant_no, user_no;

	public long getBookmark_no() {
		return bookmark_no;
	}

	public void setBookmark_no(long bookmark_no) {
		this.bookmark_no = bookmark_no;
	}

	public long getRestaurant_no() {
		return restaurant_no;
	}

	public void setRestaurant_no(long restaurant_no) {
		this.restaurant_no = restaurant_no;
	}

	public long getUser_no() {
		return user_no;
	}

	public void setUser_no(long user_no) {
		this.user_no = user_no;
	}
}
